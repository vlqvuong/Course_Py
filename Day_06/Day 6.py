# Defining and Calling Python Functions:
# Để tạo 1 function ta dùng: "def tên function():", ở trong dấu () là các giá trị ta nhập vào khi gọi function, sau đó ta để các code cần thực hiện vào đó.
def my_function():
    print("Hello")
    print("Bye")
# Nếu để vậy chạy chương trình thì không có gì xảy ra cả.
# Để thực hiện các lệnh trong Function ta phải gọi lại function đó, thì các lệnh trong function đó mới thực thi. Đây được gọi là Calling Function. Khi gặp lệnh Calling Function thì máy tính sẽ đem tất cả các cấu trúc có trong function đó ra.
my_function()
# Trong 1 function có thể có nhiều function trong đó.

# Indentation: các code nằm dưới các function và nằm thụt vào trong thì nằm trong block của function đó(tương tự như cây thư mục)

# While loop: sẽ tiếp tục nếu điều kiện đúng
# while some_thing_is_true:
# Do something repeatedly
# Vòng lặp while sẽ trở thành vòng lặp vô tận nếu điều kiện ở vòng lặp là điều luôn đúng, và đó là điều thường không mong muốn ở đa số các trường hợp. ex: while 5>3: vòng lặp này sẽ là vòng lặp vô tận.
